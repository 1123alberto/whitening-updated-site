function goToStep(stepNumber) {
    document.querySelectorAll('.booking-step').forEach(el => el.style.display = 'none');

    let stepId = `booking-step-${stepNumber}`;
    if (stepNumber === 'service') stepId = 'booking-step-service';

    const stepEl = document.getElementById(stepId);
    if (stepEl) {
        stepEl.style.display = 'block';

        // Auto-scroll to booking section for better UX on mobile
        const bookingSec = document.getElementById('booking');
        if (bookingSec && stepNumber !== 1) {
            bookingSec.scrollIntoView({ behavior: 'smooth', block: 'start' });
        }
    }

    if (stepNumber === 1) {
        renderCalendar();
    }
}
window.goToStep = goToStep; // Expose to global scope for onclick handlers

function renderCalendar() {
    const grid = document.getElementById('calendar-grid');
    const monthLabel = document.getElementById('calendar-month');
    if (!grid || !monthLabel) return;
    grid.innerHTML = '';

    // Add Day Headers
    const daysEl = ['Δευ', 'Τρι', 'Τετ', 'Πεμ', 'Παρ', 'Σαβ', 'Κυρ'];
    const daysEn = ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat', 'Sun'];
    const activeDays = currentLang === 'el' ? daysEl : daysEn;

    activeDays.forEach(d => {
        const div = document.createElement('div');
        div.className = 'day-header';
        div.textContent = d;
        grid.appendChild(div);
    });

    // Render 35 days (5 weeks)
    const today = new Date();
    today.setHours(0, 0, 0, 0);

    let monthName = "";

    for (let i = 0; i < 35; i++) {
        const date = new Date(currentWeekStart);
        date.setDate(date.getDate() + i);

        if (i % 7 === 0) {
            const m = date.toLocaleString(document.documentElement.lang, { month: 'long', year: 'numeric' });
            if (!monthName.includes(m)) monthName += (monthName ? " - " : "") + m;
        }

        const btn = document.createElement('button');
        btn.className = 'date-btn';
        btn.textContent = date.getDate();

        // Disable past dates, Sundays (0), and specific blocked dates
        const y = date.getFullYear();
        const m = String(date.getMonth() + 1).padStart(2, '0');
        const d = String(date.getDate()).padStart(2, '0');
        const dateStr = `${y}-${m}-${d}`;
        const blockedDates = ['2026-04-10', '2026-04-11', '2026-04-13', '2026-04-14', '2026-05-01'];

        if (date < today || date.getDay() === 0 || blockedDates.includes(dateStr)) {
            btn.classList.add('disabled');
        } else {
            btn.onclick = () => selectDate(date);
        }

        if (selectedDate && date.getTime() === selectedDate.getTime()) {
            btn.classList.add('active');
        }

        grid.appendChild(btn);
    }
    monthLabel.textContent = monthName;
}

function selectDate(date) {
    selectedDate = date;
    renderCalendar(); // Reflect selection
    goToStep('service');
}

function selectService(serviceKey, duration) {
    selectedDuration = duration;
    selectedServiceLabel = translations[currentLang][`service_${serviceKey}`] || serviceKey;
    fetchAndShowSlots(selectedDate);
}
window.selectService = selectService;

// Format date for display
function formatDateDisplay(date) {
    const options = { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric' };
    return date.toLocaleDateString(document.documentElement.lang, options);
}

// Format date for API (YYYY-MM-DD)
function formatDateAPI(date) {
    if (!date) return '';
    const y = date.getFullYear();
    const m = String(date.getMonth() + 1).padStart(2, '0');
    const d = String(date.getDate()).padStart(2, '0');
    return `${y}-${m}-${d}`;
}

async function fetchAndShowSlots(date) {
    document.getElementById('calendar-loader').style.display = 'flex';
    document.getElementById('selected-date-display').textContent = formatDateDisplay(date);

    const slotsGrid = document.getElementById('time-slots-grid');
    slotsGrid.innerHTML = '';

    // Saturday logic
    if (date.getDay() === 6) {
        document.getElementById('calendar-loader').style.display = 'none';
        slotsGrid.innerHTML = `<p style="grid-column: 1/-1; text-align: center;" data-i18n="sat_phone_only">${translations[currentLang].sat_phone_only}</p>`;
        goToStep(2);
        return;
    }

    try {
        let availableSlots = [];

        if (GOOGLE_SCRIPT_URL.includes("YOUR_GOOGLE_APPS_SCRIPT_URL_HERE")) {
            // Test mode simulation (hourly starting times)
            await new Promise(r => setTimeout(r, 800));
            availableSlots = [];
            const now = new Date();
            const minTime = new Date(now.getTime() + 6 * 60 * 60 * 1000);

            for (let h = 10; h <= 19; h++) {
                if (h === 14 || h === 15 || h === 16) continue;
                const slotTime = new Date(date.getFullYear(), date.getMonth(), date.getDate(), h, 0, 0);
                if (slotTime > minTime) {
                    availableSlots.push(`${h}:00`);
                }
            }
        } else {
            const response = await fetch(`${GOOGLE_SCRIPT_URL}?action=getSlots&date=${formatDateAPI(date)}&duration=${selectedDuration}`, {
                method: 'GET',
                redirect: 'follow'
            });

            if (!response.ok) {
                throw new Error(`HTTP error! status: ${response.status}`);
            }

            const contentType = response.headers.get("content-type");
            if (contentType && contentType.indexOf("application/json") !== -1) {
                const data = await response.json();
                availableSlots = data.slots || [];
            } else {
                // If it's not JSON, it's likely a Google Error page (check your deployment!)
                const text = await response.text();
                console.error("Non-JSON response from Google Apps Script. This usually means doGet is missing or the script isn't deployed as 'Anyone'.", text.substring(0, 500));
                throw new Error("Invalid response format from server.");
            }
        }

        if (availableSlots.length === 0) {
            slotsGrid.innerHTML = `<p style="grid-column: 1/-1; text-align: center;">${currentLang === 'el' ? 'Δεν υπάρχουν διαθέσιμες ώρες για αυτή την ημερομηνία.' : 'No slots available for this date.'}</p>`;
        } else {
            availableSlots.forEach(time => {
                const btn = document.createElement('button');
                btn.className = 'time-slot-btn';
                btn.textContent = time;
                btn.onclick = () => selectTime(time);
                slotsGrid.appendChild(btn);
            });
        }

    } catch (error) {
        console.error("CRITICAL: Error fetching slots:", error);
        slotsGrid.innerHTML = `<p style="grid-column: 1/-1; text-align: center; color: red;">Error loading availability. Please check console (F12).</p>`;
    } finally {
        document.getElementById('calendar-loader').style.display = 'none';
        goToStep(2);
    }
}

function selectTime(time) {
    selectedTime = time;
    document.getElementById('final-date-time').textContent = `${formatDateDisplay(selectedDate)} | ${time} (${selectedServiceLabel})`;
    goToStep(3);
}

document.addEventListener("DOMContentLoaded", () => {
    // Week navigation
    const prevBtn = document.getElementById('prev-week');
    const nextBtn = document.getElementById('next-week');

    if (prevBtn) {
        prevBtn.onclick = () => {
            const today = new Date();
            today.setHours(0, 0, 0, 0);

            // Limit how far back users can go
            const minStart = new Date(today);
            minStart.setDate(today.getDate() - 7);

            currentWeekStart.setDate(currentWeekStart.getDate() - 28);
            if (currentWeekStart < minStart) {
                // Return to previous Monday
                const d = today.getDay();
                const diff = today.getDate() - d + (d === 0 ? -6 : 1);
                currentWeekStart.setDate(diff);
            }
            renderCalendar();
        };
    }

    if (nextBtn) {
        nextBtn.onclick = () => {
            currentWeekStart.setDate(currentWeekStart.getDate() + 28);
            renderCalendar();
        };
    }

    // Initial render
    renderCalendar();

    // Form Submission
    const bookingForm = document.getElementById('booking-form');
    if (bookingForm) {
        bookingForm.addEventListener('submit', async (e) => {
            e.preventDefault();

            const btn = document.getElementById('submit-booking-btn');
            const loader = document.getElementById('submit-loader');

            const payload = {
                action: 'book',
                date: formatDateAPI(selectedDate),
                time: selectedTime,
                duration: selectedDuration,
                service: selectedServiceLabel,
                name: document.getElementById('b-name').value,
                email: document.getElementById('b-email').value,
                phone: document.getElementById('b-phone').value,
                rescheduleUid: window.rescheduleUid || null
            };

            btn.disabled = true;
            loader.style.display = 'flex';

            try {
                if (!GOOGLE_SCRIPT_URL.includes("YOUR_GOOGLE_APPS_SCRIPT_URL_HERE")) {
                    await fetch(GOOGLE_SCRIPT_URL, {
                        method: 'POST',
                        mode: 'no-cors',
                        headers: { 'Content-Type': 'application/json' },
                        body: JSON.stringify(payload)
                    });
                } else {
                    await new Promise(r => setTimeout(r, 1500)); // Test mode simulation
                }
                goToStep(4);
            } catch (error) {
                alert(currentLang === 'el' ? "Σφάλμα συστήματος. Δοκιμάστε ξανά." : "System error. Please try again.");
            } finally {
                btn.disabled = false;
                loader.style.display = 'none';
            }
        });
    }
});
